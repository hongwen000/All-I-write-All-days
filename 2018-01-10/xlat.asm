DATA 	SEGMENT
		SQUARE 	DB 00H, 01H, 04H, 09H, 10H, 19H, 24H, 31H, 40H, 51H, 64H
		ENDL	DB ' ', 0DH, 0AH, '$'
DATA	ENDS

CODE 	SEGMENT
		ASSUME DS:DATA, CS:CODE
BEGIN:
		MOV AX, DATA
		MOV DS, AX
		LEA BX, SQUARE
		MOV AH, 01H
		INT 21H
		;❌❌❌❌
		;不能直接查表转换，这里AL是输入数字的ASCII码
		AND AL, 0FH
		XLAT SQUARE
		;MOV AL, 09H
		MOV CL, AL
		MOV DH, 0
		LEA DX, ENDL
		MOV AH, 09H
		INT 21H
		MOV AL, CL
		MOV AH, 0
		MOV CX, 0
		MOV BX, 0AH
L1:		
		MOV DX, 0
		DIV BX
		PUSH DX
		INC CX
		CMP AX, 0
		JNZ L1
L2:		
		POP DX
		OR	DL, 30H
		MOV AH, 02H
		INT 21H
		LOOP L2
DONE:	
		MOV AH, 4CH
		INT 21H
CODE	ENDS
		END BEGIN